package Assignment_Ajay;//Write a Java program to get the length of a given string.
//Sample Output:
//The string length of 'example.com' is: 11

public class StringLength {
    public static void main(String[] args) {
        // Define the string
        String str = "example.com";

        // Get the length of the string
        int length = str.length();

        // Print the result
        System.out.println("The string length of '" + str + "' is: " + length);
    }
}

